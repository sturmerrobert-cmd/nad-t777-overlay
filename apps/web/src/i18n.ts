/**
 * Tiny i18n: a module-level current language + a `t(key, vars)` lookup.
 *
 * Components just call `t('key')`. Language changes re-render the whole tree
 * because the App root keeps `lang` in state and calls setLang() — the cascade
 * re-runs every t() with the new language (no per-component hook needed).
 */

export type Lang = 'pl' | 'en' | 'de';
export const LANGS: { id: Lang; label: string }[] = [
  { id: 'pl', label: 'PL' },
  { id: 'en', label: 'EN' },
  { id: 'de', label: 'DE' },
];

const STORE_KEY = 'nad.lang';

function detect(): Lang {
  try {
    const saved = localStorage.getItem(STORE_KEY) as Lang | null;
    if (saved && ['pl', 'en', 'de'].includes(saved)) return saved;
    const nav = navigator.language.slice(0, 2).toLowerCase();
    if (nav === 'de') return 'de';
    if (nav === 'en') return 'en';
  } catch {
    /* ignore */
  }
  return 'pl';
}

let _lang: Lang = detect();
export const getLang = (): Lang => _lang;
export function setLang(l: Lang): void {
  _lang = l;
  try {
    localStorage.setItem(STORE_KEY, l);
  } catch {
    /* ignore */
  }
}

/** Translate `key`, falling back EN → key. `{name}` placeholders from `vars`. */
export function t(key: string, vars?: Record<string, string | number>): string {
  const dict = STR[_lang] ?? STR.en;
  let s = dict[key] ?? STR.en[key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  return s;
}

